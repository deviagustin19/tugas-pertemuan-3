class method_fungsi {

    // Method fungsi untuk menjumlahkan dua angka
    public static int tambah(int a, int b) {
        return a + b;  // Mengembalikan hasil penjumlahan
    }

    public static void main(String[] args) {
        // Memanggil method fungsi dan menyimpan hasilnya
        int hasil = tambah(5, 3);
        System.out.println("Hasil penjumlahan: " + hasil);  // Menampilkan hasil
    }
}
  

